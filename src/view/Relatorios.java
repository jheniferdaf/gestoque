    private void botaoConfirmarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoConfirmarMouseClicked
        if (grupoRadioButton.getSelection() == null){
            JOptionPane.showMessageDialog(null, "Nenhuma op√ß√£o selecionada.");
        } else {
            if (pesquisarData.isSelected()){
                try {
                    JRResultSetDataSource resultSet;
                    if (data1.getDate().getTime() < data2.getDate().getTime()){
                        resultSet = new JRResultSetDataSource(MovimentacaoService.buscarMovimentacaoData(data1.getDate(), data2.getDate()));
                    } else {
                        resultSet = new JRResultSetDataSource(MovimentacaoService.buscarMovimentacaoData(data2.getDate(), data1.getDate()));
                    }
                    JasperPrint jpPrint = JasperFillManager.fillReport("Relatorios/relatorioPorData.jasper", new HashMap<>(), resultSet);
                    JasperViewer jv = new JasperViewer(jpPrint, false);
                    jv.setVisible(true);
                    jv.toFront();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Problema ao gerar relat√≥rio.");
                    ex.printStackTrace();
                }
                System.out.println("Data1: " + data1.getDate());
                System.out.println("Data2: " + data2.getDate());
            } else {
                try{        
                    JRResultSetDataSource resultSet = new JRResultSetDataSource(MovimentacaoService.buscaMovimentacaoFornecedor(listaFornecedores.get(fornecedores.getSelectedIndex()).getCnpj()));

                        JasperPrint jpPrint = JasperFillManager.fillReport("Relatorios/relatorioPorFornecedor.jasper", new HashMap<>(), resultSet);
                        JasperViewer jv = new JasperViewer(jpPrint, false);
                        jv.setVisible(true);
                        jv.toFront();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Problema ao gerar relat√≥rio.");
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_botaoConfirmarMouseClicked

